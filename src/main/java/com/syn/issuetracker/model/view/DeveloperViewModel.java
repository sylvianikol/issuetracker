package com.syn.issuetracker.model.view;

public class DeveloperViewModel {

    private String id;
    private String name;

    public DeveloperViewModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
